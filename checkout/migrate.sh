#!/bin/sh

DIR_MIGRATIONS=migrations
PATH_TO_CONFIG=config.yml

parse_yaml() {
   local prefix=$2
   local s='[[:space:]]*' w='[a-zA-Z0-9_]*' fs=$(echo @|tr @ '\034')
   sed -ne "s|^\($s\)\($w\)$s:$s\"\(.*\)\"$s\$|\1$fs\2$fs\3|p" \
        -e "s|^\($s\)\($w\)$s:$s\(.*\)$s\$|\1$fs\2$fs\3|p"  $1 |
   awk -F$fs '{
      indent = length($1)/2;
      vname[indent] = $2;
      for (i in vname) {if (i > indent) {delete vname[i]}}
      if (length($3) > 0) {
         vn=""; for (i=0; i<indent; i++) {vn=(vn)(vname[i])("_")}
         printf("%s%s%s=\"%s\"\n", "'$prefix'",vn, $2, $3);
      }
   }'
}

eval $(parse_yaml $PATH_TO_CONFIG "config_")

URL="host=$config_database_host port=$config_database_port user=$config_database_user password=$config_database_password dbname=$config_database_name"

goose -dir $DIR_MIGRATIONS postgres "$URL" status

goose -dir $DIR_MIGRATIONS postgres "$URL" up
